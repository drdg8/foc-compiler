int main() {
    int a = 10;
    int b = 5;

    // If-Else Branching
    if (a > b) {
        printf("a is greater than b\n");
    } else {
        printf("b is greater than a\n");
    }

    // Switch Case
    switch (a) {
        case 10:{
            printf("a is 10\n");
            break;
        }
        case 5:{
            printf("a is 5\n");
            break;
        }
    }

    return 0;
}
